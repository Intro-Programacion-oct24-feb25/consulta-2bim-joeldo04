def producto_recursivo(a):
    if a == 1:
        return 1
    else:
        return a * producto_recursivo(a - 1)

if __name__ == "__main__":
    print("Ejemplo de Recursividad en Python\n")
    numero = int(input("Ingrese un numero: "))
    resultado = producto_recursivo(numero)
    print(f"El producto de los numeros hasta {numero} es: {resultado}")
    